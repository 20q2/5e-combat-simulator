export function getMonsterById(id: string): Monster | undefined {
  return monsters.find(m => m.id === id)
}

export function getAllMonsters(): Monster[] {
  return monsters
}

export function getMonstersByCR(cr: number): Monster[] {
  return monsters.filter(m => m.challengeRating === cr)
}

export function getMonstersByType(type: string): Monster[] {
  return monsters.filter(m => m.type.toLowerCase() === type.toLowerCase())
}

export function getMonstersUpToCR(maxCR: number): Monster[] {
  return monsters.filter(m => m.challengeRating <= maxCR)
}
