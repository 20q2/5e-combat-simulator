export function getWeaponById(id: string): Weapon | undefined {
  return weapons.find(w => w.id === id)
}

export function getArmorById(id: string): Armor | undefined {
  return armors.find(a => a.id === id)
}

export function getAllWeapons(): Weapon[] {
  return weapons
}

export function getAllArmors(): Armor[] {
  return armors
}

export function getWeaponsByCategory(category: 'simple' | 'martial'): Weapon[] {
  return weapons.filter(w => w.category === category)
}

export function getWeaponsByType(type: 'melee' | 'ranged'): Weapon[] {
  return weapons.filter(w => w.type === type)
}

export function getArmorsByCategory(category: 'light' | 'medium' | 'heavy' | 'shield'): Armor[] {
  return armors.filter(a => a.category === category)
}
